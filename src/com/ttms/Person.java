package com.ttms;

public class Person {
    private int id;
    private String name;
    private String pEmailAdd;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getpEmailAdd() {
        return pEmailAdd;
    }

    public void setpEmailAdd(String pEmailAdd) {
        this.pEmailAdd = pEmailAdd;
    }
}
